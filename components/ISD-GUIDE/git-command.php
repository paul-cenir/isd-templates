<h2>Git commands</h2>
<ol>
    <li>
        git checkout --> gamit sa paglipat sa branch
    </li>
    <li>
        git pull  --> pag kuha lahat ng gawa nung mga brother and sister ko (nasa release branch dapat)
    </li>
    <li>
        git checkout -b  --> pag gawa ng branch or folder
    </li>
    <li>
        git commit -m  --> Message kung ano ginawa mo
    </li>
    <li>
        git push  -->  ginagamit ito para yung code ko mapunta sa branch.
    </li>
    <li>
        git merge -->  para isama/magsanib  yung gawa nyo (Note:  git push always after ng git merge )
    </li>

</ol>

